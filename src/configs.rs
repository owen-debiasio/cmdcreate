use std::sync::LazyLock;
use toml::{Value, from_str};

use crate::utils::fs::{PATHS, overwrite_file, path_exists, read_file_to_string};

const AUTO_GEN: &str = "
# Auto-generated by cmdcreate
#
# You can find available configurations at:
# https://github.com/owen-debiasio/cmdcreate/blob/main/docs/resources/config_example.toml
";

pub fn init_configs() {
    let config_file_path = &PATHS.configuration_file;

    if path_exists(config_file_path) {
        return;
    }
    // AUTO_GEN is the autogenerated message on as newly created file
    // TODO: Fix this not working
    overwrite_file(config_file_path, AUTO_GEN).expect("Failed to overwrite file");
}

static CONFIG: LazyLock<Value> = LazyLock::new(|| {
    let config_file_contents: &str = &read_file_to_string(&PATHS.configuration_file);

    from_str(config_file_contents).expect("Failed to read file")
});

pub fn load_configuration(cat: &str, var: &str, default: &str) -> String {
    let (config_category, config_value, default_value) = (cat, var, default);

    CONFIG
        .get(config_category)
        .and_then(|category| category.get(config_value))
        .and_then(|value| value.as_str())
        .unwrap_or(default_value)
        .to_string()
}
