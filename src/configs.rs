use std::sync::LazyLock;
use toml::Value;

use crate::utils::fs::{PATHS, overwrite_file, path_exists, read_file_to_string};

const AUTO_GEN: &str = "
# Auto-generated by cmdcreate
#
# You can find available configurations at:
# https://github.com/owen-debiasio/cmdcreate/blob/main/docs/resources/config_example.toml
";

pub fn init_configs() {
    if path_exists(&PATHS.configs) {
        return;
    }

    overwrite_file(&PATHS.configs, AUTO_GEN).expect("Failed to overwrite file");
}

static CONFIG: LazyLock<Value> = LazyLock::new(|| {
    toml::from_str(&read_file_to_string(&PATHS.configs)).expect("Failed to read file")
});

pub fn load(cat: &str, var: &str, default: &str) -> String {
    CONFIG
        .get(cat)
        .and_then(|c| c.get(var))
        .and_then(|v| v.as_str())
        .unwrap_or(default)
        .to_string()
}
